"""Pure build-time transforms that produce the precomputed expression artifacts.

This is the **build-time** half of the expression layer — the public counterpart
to the read-time accessors in :mod:`oncoref.expression`. Each shipped shard
dataset is generated by one core here:

* :func:`cohort_percentile_vectors` → the ``cancer-reference-expression-percentiles``
  shards (per-gene cohort breakpoints, :data:`PERCENTILE_BREAKPOINTS`);
* :func:`cohort_medoids` → the ``cancer-reference-expression-representatives`` shards
  (representative sample picks spanning within-cohort variation);
* :func:`within_sample_top_fractions` → the within-sample-top shards
  (:data:`WITHIN_SAMPLE_THRESHOLDS`);
* :func:`sum_proteoform_tpm` → the proteoform-summed artifacts.

Each shard's :class:`~oncoref.expression.ShardDataset` names its core via
``build_attr``, so :mod:`oncoref.expression` can regenerate a missing cohort
on demand by calling back into this module.

Kept deliberately dependency-light (pandas/numpy only) and free of I/O, so the
cores are unit-testable without any data bundle (see ``tests/test_build_generators``)
and the ``scripts/generate_*`` / ``scripts/rebuild_expression_artifacts`` wrappers
stay thin. Renamed from the former private ``_build`` module so it can be
referenced and documented as the public build-time API.
"""

