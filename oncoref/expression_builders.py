def recount3_gene_sums_to_tpm(
    gene_sums: pd.DataFrame,
    bp_length: pd.Series,
) -> pd.DataFrame:
    """Length-normalize recount3 coverage gene-sums to per-sample TPM.

    recount3 gene-sums are coverage area over exonic bases, so this divides by
    recount3's exonic ``bp_length`` and then renormalizes each sample to one
    million. Version and ``_PAR_Y`` duplicates are collapsed before normalization.
    """
    gs = gene_sums.copy()
    gs.index = [_recount3_gene_id(idx) for idx in gs.index]
    gs = gs.groupby(level=0).sum()
    lengths_kb = (pd.Series(bp_length, dtype=float) / 1000.0).rename("gene_length_kb")
    frame = gs.reset_index().rename(columns={"index": "Ensembl_Gene_ID"})
    tpm = normalize_source_matrix_to_tpm(
        frame,
        unit="raw_counts",
        row_id_col="Ensembl_Gene_ID",
        value_cols=list(gs.columns),
        gene_lengths_kb=lengths_kb,
    )
    return tpm.set_index("Ensembl_Gene_ID")[list(gs.columns)]


def parse_recount3_sample_attributes(packed: str) -> dict[str, str]:
    """Unpack recount3's ``key;;value|key;;value`` sample attribute field."""
    out: dict[str, str] = {}
    for item in str(packed).split("|"):
        if ";;" not in item:
            continue
        key, value = item.split(";;", 1)
        out[key.strip()] = value.strip()
    return out


def aggregate_recount3_runs_to_samples(
    gene_sums: pd.DataFrame,
    metadata: pd.DataFrame,
    *,
    keep_runs: set[str] | None = None,
    run_col: str = "external_id",
    sample_col: str = "sample_acc",
) -> tuple[pd.DataFrame, pd.DataFrame]:
    """Sum recount3 run-level coverage columns into biological samples."""
    meta = metadata.copy()
    meta[run_col] = meta[run_col].astype(str)
    runs = [col for col in gene_sums.columns if col in set(meta[run_col])]
    if keep_runs is not None:
        runs = [run for run in runs if run in keep_runs]
    meta = meta[meta[run_col].isin(runs)].copy()
    run_to_sample = dict(zip(meta[run_col], meta[sample_col].astype(str)))
    sample_gene_sums = gene_sums[runs].copy()
    sample_gene_sums.columns = [run_to_sample[col] for col in sample_gene_sums.columns]
    sample_gene_sums = sample_gene_sums.T.groupby(level=0).sum().T
    sample_meta = meta.drop_duplicates(sample_col).copy()
    sample_meta[sample_col] = sample_meta[sample_col].astype(str)
    sample_meta = sample_meta.set_index(sample_col).reindex(sample_gene_sums.columns)
    return sample_gene_sums, sample_meta


def _artifact_stem(value: str) -> str:
    return "".join(c if c.isalnum() or c in "._-" else "_" for c in value)


def _atomic_write(path: Path, writer: Callable[[Path], None]) -> None:
    """Write ``path`` via a same-directory temp file, then atomically replace."""
    path.parent.mkdir(parents=True, exist_ok=True)
    fd, tmp_name = tempfile.mkstemp(
        prefix=f".{path.name}.",
        suffix=".tmp",
        dir=path.parent,
    )
    os.close(fd)
    tmp_path = Path(tmp_name)
    try:
        writer(tmp_path)
        os.replace(tmp_path, path)
    finally:
        tmp_path.unlink(missing_ok=True)


def _write_csv_atomic(df: pd.DataFrame, path: Path) -> None:
    _atomic_write(path, lambda tmp_path: df.to_csv(tmp_path, index=False))


def _write_parquet_atomic(df: pd.DataFrame, path: Path) -> None:
    _atomic_write(path, lambda tmp_path: df.to_parquet(tmp_path, index=False))


def medulloblastoma_subgroup_sample_ids(matrix: pd.DataFrame) -> dict[str, list[str]]:
    """Assign MBL samples by the historical four-marker maximum-TPM rule.

    WIF1, GLI2, MYC, and KCNA1 identify WNT, SHH, Group 3, and Group 4,
    respectively. Every sample must have one unambiguous winning marker; ties,
    missing markers, and nonnumeric marker values are rejected rather than routed
    by an arbitrary column order.
    """
    required_columns = {"Ensembl_Gene_ID", "Symbol"}
    missing_columns = required_columns - set(matrix.columns)
    if missing_columns:
        raise ValueError(
            "MBL source matrix lacks required columns: " + ", ".join(sorted(missing_columns))
        )

    samples = sample_columns(matrix)
    if not samples:
        raise ValueError("MBL source matrix has no sample columns")

    marker_ids = set(MEDULLOBLASTOMA_SUBGROUP_MARKER_GENE_IDS.values())
    gene_ids = matrix["Ensembl_Gene_ID"].astype(str).map(unversioned)
    marker_rows = matrix.loc[gene_ids.isin(marker_ids), ["Ensembl_Gene_ID", *samples]].copy()
    marker_rows["Ensembl_Gene_ID"] = marker_rows["Ensembl_Gene_ID"].astype(str).map(unversioned)
    marker_counts = marker_rows["Ensembl_Gene_ID"].value_counts()
    invalid_markers = sorted(
        marker_id for marker_id in marker_ids if marker_counts.get(marker_id) != 1
    )
    if invalid_markers:
        raise ValueError(
            "MBL source matrix must contain one row for each subgroup marker; invalid: "
            + ", ".join(invalid_markers)
        )

    marker_expression = marker_rows.set_index("Ensembl_Gene_ID")[samples].T
    marker_expression = marker_expression.apply(pd.to_numeric, errors="coerce")
    finite_values = np.isfinite(marker_expression.to_numpy(dtype=float))
    if not finite_values.all():
        bad_samples = marker_expression.index[~finite_values.all(axis=1)].tolist()
        raise ValueError(
            "MBL subgroup marker expression is missing, nonnumeric, or non-finite "
            "for sample(s): " + ", ".join(bad_samples[:5])
        )

    winning_markers = marker_expression.eq(marker_expression.max(axis=1), axis=0)
    ambiguous_samples = winning_markers.index[winning_markers.sum(axis=1) != 1].tolist()
    if ambiguous_samples:
        raise ValueError(
            "MBL subgroup marker maximum is tied for sample(s): " + ", ".join(ambiguous_samples[:5])
        )

    winning_gene_id = winning_markers.idxmax(axis=1)
    return {
        code: winning_gene_id.index[winning_gene_id.eq(marker_id)].tolist()
        for code, marker_id in MEDULLOBLASTOMA_SUBGROUP_MARKER_GENE_IDS.items()
    }


def medulloblastoma_subgroup_matrices(matrix: pd.DataFrame) -> dict[str, pd.DataFrame]:
    """Split a canonical MBL per-sample TPM matrix into four subgroup matrices."""
    groups = medulloblastoma_subgroup_sample_ids(matrix)
    identifiers = id_columns(matrix)
    return {code: matrix[[*identifiers, *samples]].copy() for code, samples in groups.items()}


def _reconcile_per_code_artifacts(out_dir: Path, current_codes: Iterable[str]) -> None:
    """Remove stale per-code matrix/QC sidecars from a per-source output directory."""
    current_stems = {_artifact_stem(code) for code in current_codes}
    for suffix in ("_per_sample_tpm.parquet", "_sample_qc.csv"):
        for path in out_dir.glob(f"*{suffix}"):
            code_stem = path.name[: -len(suffix)]
            if code_stem not in current_stems:
                path.unlink(missing_ok=True)


def split_source_matrix_by_code(
    matrix: pd.DataFrame,
    cancer_code: str | list[str],
    *,
    sample_to_cancer_code: Callable[[str], str | None] | None = None,
) -> dict[str, list[str]]:
    """Route canonicalized sample columns to cancer codes."""
    samples = sample_columns(matrix)
    if sample_to_cancer_code is None:
        if isinstance(cancer_code, list):
            raise ValueError("cancer_code is a list but no sample_to_cancer_code was provided")
        return {str(cancer_code): samples}

    out: dict[str, list[str]] = {}
    for sample in samples:
        code = sample_to_cancer_code(str(sample))
        if code is not None:
            out.setdefault(str(code), []).append(sample)
    return out


def build_source_matrices(
    source: GeoMatrixSource,
    *,
    cache_dir: str | Path,
    output_dir: str | Path | None = None,
    source_path: str | Path | None = None,
    force_download: bool = False,
    gene_lengths_kb: pd.Series | Mapping[str, float] | None = None,
    high_expression_threshold: float = 1.0,
) -> SourceMatrixBuildResult:
    """Build oncoref source-matrix artifacts for one generic expression source.

    Output matrices are written as ``<CODE>_per_sample_tpm.parquet`` under
    ``output_dir`` (default ``cache_dir / "derived"``), matching the layout that
    :mod:`scripts.stage_source_matrices` consumes before release upload. Sidecars
    include source-row mapping audit, source-value parse diagnostics, and per-sample
    QC manifests. Samples are not dropped here; read/build-time consumers choose
    ``all``/``pass``/``pass_or_warn`` filtering from the QC manifest.
    ``output_dir`` is treated as this source's derived-artifact directory; after a
    successful build, stale per-code matrix/QC sidecars in it are removed.
    """
    cache = Path(cache_dir)
    out_dir = Path(output_dir) if output_dir is not None else cache / "derived"
    out_dir.mkdir(parents=True, exist_ok=True)

    file_path = Path(source_path) if source_path is not None else cache / source.file_name
    if not file_path.exists() or force_download:
        if not source.file_url:
            raise ValueError("source_path is absent and GeoMatrixSource.file_url is not set")
        file_path = _download(source.file_url, file_path, force=force_download)

    raw = read_source_expression_matrix(
        file_path,
        sep=source.sep,
        gene_id_col=source.gene_id_col,
        symbol_col=source.symbol_col,
        drop_cols=source.drop_cols,
        transposed=source.transposed,
    )
    row_id_col = raw.attrs["row_id_col"]
    symbol_col = raw.attrs.get("symbol_col")
    raw_value_cols = source_expression_value_columns(
        raw, row_id_col=row_id_col, symbol_col=symbol_col
    )
    if source.sample_filter is not None:
        keep = source.sample_filter(raw_value_cols)
        missing = [c for c in keep if c not in raw.columns]
        if missing:
            raise ValueError(f"sample_filter returned missing sample columns: {missing}")
        raw = raw[[c for c in (row_id_col, symbol_col) if c is not None] + keep].copy()
        raw.attrs["row_id_col"] = row_id_col
        raw.attrs["symbol_col"] = symbol_col
        raw_value_cols = keep
    if not raw_value_cols:
        raise ValueError("source matrix has no sample/value columns")

    _, parse_diagnostics = coerce_source_expression_values(
        raw, value_cols=raw_value_cols, row_id_col=row_id_col, symbol_col=symbol_col
    )
    tpm = normalize_source_matrix_to_tpm(
        raw,
        unit=source.unit,
        row_id_col=row_id_col,
        symbol_col=symbol_col,
        value_cols=raw_value_cols,
        gene_lengths_kb=gene_lengths_kb,
    )
    matrix, audit = canonicalize_source_gene_matrix(
        tpm,
        row_id_col=row_id_col,
        symbol_col=symbol_col,
        value_cols=raw_value_cols,
        high_expression_threshold=high_expression_threshold,
    )
    matrix.attrs["source_value_parse_diagnostics"] = parse_diagnostics

    stem = _artifact_stem(source.source_cohort)
    sidecar_paths: dict[str, Path] = {}
    audit_path = out_dir / f"{stem}_mapping_audit.csv"
    parse_path = out_dir / f"{stem}_parse_diagnostics.csv"
    _write_csv_atomic(audit, audit_path)
    _write_csv_atomic(parse_diagnostics, parse_path)
    sidecar_paths["mapping_audit"] = audit_path
    sidecar_paths["parse_diagnostics"] = parse_path

    meta = source_metadata(
        source_cohort=source.source_cohort,
        source_type=source.source_project,
        unit=source.unit,
        source_scale_class=source.source_scale_class,
        linear_tpm_comparable=source.linear_tpm_comparable,
        tpm_proxy=source.tpm_proxy,
    )

    from .expression import sample_expression_qc_from_matrix

    matrices: dict[str, pd.DataFrame] = {}
    matrix_paths: dict[str, Path] = {}
    qc_frames: list[pd.DataFrame] = []
    summary_frames: list[pd.DataFrame] = []
    for code, cols in split_source_matrix_by_code(
        matrix,
        source.cancer_code,
        sample_to_cancer_code=source.sample_to_cancer_code,
    ).items():
        if not cols:
            continue
        sub = matrix[[*id_columns(matrix), *cols]].copy()
        sub.attrs = {}
        path = out_dir / f"{_artifact_stem(code)}_per_sample_tpm.parquet"
        _write_parquet_atomic(sub, path)
        qc = sample_expression_qc_from_matrix(sub, cancer_type=code, source_metadata=meta)
        qc_path = out_dir / f"{_artifact_stem(code)}_sample_qc.csv"
        _write_csv_atomic(qc, qc_path)
        matrices[code] = sub
        matrix_paths[code] = path
        sidecar_paths[f"{code}_sample_qc"] = qc_path
        qc_frames.append(qc)
        summary_frames.append(
            summarize_source_matrix(
                sub,
                cancer_code=code,
                source=source,
                native_unit=source.unit,
            )
        )

    if not matrices:
        raise ValueError("no samples were routed to a cancer code")
    sample_qc = pd.concat(qc_frames, ignore_index=True) if qc_frames else pd.DataFrame()
    summary_rows = (
        pd.concat(summary_frames, ignore_index=True)
        if summary_frames
        else pd.DataFrame(columns=list(REFERENCE_EXPRESSION_COLUMNS))
    )
    summary_path = out_dir / f"{stem}_summary_rows.csv"
    _write_csv_atomic(summary_rows, summary_path)
    _reconcile_per_code_artifacts(out_dir, matrices)
    sidecar_paths["summary_rows"] = summary_path
    return SourceMatrixBuildResult(
        source=source,
        matrices=matrices,
        matrix_paths=matrix_paths,
        summary_rows=summary_rows,
        mapping_audit=audit,
        parse_diagnostics=parse_diagnostics,
        sample_qc=sample_qc,
        sidecar_paths=sidecar_paths,
    )


def build_recount3_source_matrices(
    source: Recount3Source,
    *,
    cache_dir: str | Path,
    output_dir: str | Path | None = None,
    force_download: bool = False,
    high_expression_threshold: float = 1.0,
) -> SourceMatrixBuildResult:
    """Build oncoref source-matrix artifacts for one recount3 study.

    recount3 inputs are run-level coverage gene-sums, not ordinary expression
    matrices. This builder fetches annotation, gene sums, and SRA metadata,
    routes runs by source-specific metadata rules, sums runs into biological
    samples, converts coverage gene-sums to TPM, canonicalizes genes, and writes
    the same per-code parquet + audit/QC sidecars as :func:`build_source_matrices`.
    """
    cache = Path(cache_dir)
    out_dir = Path(output_dir) if output_dir is not None else cache / "derived"
    out_dir.mkdir(parents=True, exist_ok=True)

    if force_download:
        annotation = fetch_recount3_gene_annotation(cache, force_download=True)
        gene_sums = fetch_recount3_gene_sums(source.srp, cache, force_download=True)
        metadata = fetch_recount3_sample_metadata(source.srp, cache, force_download=True)
    else:
        annotation = fetch_recount3_gene_annotation(cache)
        gene_sums = fetch_recount3_gene_sums(source.srp, cache)
        metadata = fetch_recount3_sample_metadata(source.srp, cache)
    route = source.sample_to_cancer_code
    if route is None:
        route = _compile_recount3_sample_route(None, source.cancer_code)

    if "external_id" not in metadata.columns or "sample_acc" not in metadata.columns:
        raise ValueError("recount3 metadata must include external_id and sample_acc columns")
    attr_values = (
        metadata["sample_attributes"]
        if "sample_attributes" in metadata.columns
        else pd.Series([""] * len(metadata), index=metadata.index)
    )
    title_values = (
        metadata["sample_title"]
        if "sample_title" in metadata.columns
        else pd.Series([""] * len(metadata), index=metadata.index)
    )
    attrs = {
        str(run): parse_recount3_sample_attributes(raw)
        for run, raw in zip(metadata["external_id"], attr_values)
    }
    titles = dict(zip(metadata["external_id"].astype(str), title_values))
    run_code = {run: route(attrs.get(run, {}), str(titles.get(run, ""))) for run in attrs}
    keep_runs = {run for run, code in run_code.items() if code is not None}
    if not keep_runs:
        raise ValueError(f"no recount3 runs routed for source {source.source_id!r}")

    sample_gene_sums, sample_meta = aggregate_recount3_runs_to_samples(
        gene_sums,
        metadata,
        keep_runs=keep_runs,
    )
    sample_code: dict[str, str] = {}
    for sample_id, row in sample_meta.iterrows():
        code = run_code.get(str(row["external_id"]))
        if code is not None:
            sample_code[str(sample_id)] = str(code)
    counts = pd.Series(list(sample_code.values()), dtype="object").value_counts().to_dict()
    for code, expected in (source.expected_n or {}).items():
        got = int(counts.get(code, 0))
        if got != int(expected):
            raise ValueError(f"{source.source_id} routed {code} n={got}; expected {expected}")

    tpm = recount3_gene_sums_to_tpm(sample_gene_sums, annotation["bp_length"])
    symbol = annotation["Symbol"].reindex(tpm.index).fillna(pd.Series(tpm.index, index=tpm.index))
    tpm_frame = pd.DataFrame(
        {
            "Ensembl_Gene_ID": tpm.index.astype(str),
            "Symbol": symbol.to_numpy(),
        }
    )
    for col in tpm.columns:
        tpm_frame[str(col)] = tpm[col].to_numpy(dtype=float)

    value_cols = [str(col) for col in tpm.columns]
    _, parse_diagnostics = coerce_source_expression_values(
        tpm_frame,
        value_cols=value_cols,
        row_id_col="Ensembl_Gene_ID",
        symbol_col="Symbol",
    )
    matrix, audit = canonicalize_source_gene_matrix(
        tpm_frame,
        row_id_col="Ensembl_Gene_ID",
        symbol_col="Symbol",
        value_cols=value_cols,
        high_expression_threshold=high_expression_threshold,
    )
    matrix.attrs["source_value_parse_diagnostics"] = parse_diagnostics

    stem = _artifact_stem(source.source_cohort)
    sidecar_paths: dict[str, Path] = {}
    audit_path = out_dir / f"{stem}_mapping_audit.csv"
    parse_path = out_dir / f"{stem}_parse_diagnostics.csv"
    _write_csv_atomic(audit, audit_path)
    _write_csv_atomic(parse_diagnostics, parse_path)
    sidecar_paths["mapping_audit"] = audit_path
    sidecar_paths["parse_diagnostics"] = parse_path

    meta = source_metadata(
        source_cohort=source.source_cohort,
        source_type=RECOUNT3_SOURCE_TYPE,
        unit="recount3 gene_sums to TPM",
        source_scale_class="linear_rnaseq_tpm",
        linear_tpm_comparable=True,
        tpm_proxy=False,
    )

    from .expression import sample_expression_qc_from_matrix

    matrices: dict[str, pd.DataFrame] = {}
    matrix_paths: dict[str, Path] = {}
    qc_frames: list[pd.DataFrame] = []
    summary_frames: list[pd.DataFrame] = []
    for code, cols in split_source_matrix_by_code(
        matrix,
        source.cancer_code,
        sample_to_cancer_code=lambda sample: sample_code.get(str(sample)),
    ).items():
        if not cols:
            continue
        sub = matrix[[*id_columns(matrix), *cols]].copy()
        sub.attrs = {}
        path = out_dir / f"{_artifact_stem(code)}_per_sample_tpm.parquet"
        _write_parquet_atomic(sub, path)
        qc = sample_expression_qc_from_matrix(sub, cancer_type=code, source_metadata=meta)
        qc_path = out_dir / f"{_artifact_stem(code)}_sample_qc.csv"
        _write_csv_atomic(qc, qc_path)
        matrices[code] = sub
        matrix_paths[code] = path
        sidecar_paths[f"{code}_sample_qc"] = qc_path
        qc_frames.append(qc)
        summary_frames.append(
            summarize_source_matrix(
                sub,
                cancer_code=code,
                source=source,
                native_unit="recount3_gene_sums",
            )
        )

    if not matrices:
        raise ValueError("no recount3 samples were routed to a cancer code")
    sample_qc = pd.concat(qc_frames, ignore_index=True) if qc_frames else pd.DataFrame()
    summary_rows = (
        pd.concat(summary_frames, ignore_index=True)
        if summary_frames
        else pd.DataFrame(columns=list(REFERENCE_EXPRESSION_COLUMNS))
    )
    summary_path = out_dir / f"{stem}_summary_rows.csv"
    _write_csv_atomic(summary_rows, summary_path)
    _reconcile_per_code_artifacts(out_dir, matrices)
    sidecar_paths["summary_rows"] = summary_path
    return SourceMatrixBuildResult(
        source=source,
        matrices=matrices,
        matrix_paths=matrix_paths,
        summary_rows=summary_rows,
        mapping_audit=audit,
        parse_diagnostics=parse_diagnostics,
        sample_qc=sample_qc,
        sidecar_paths=sidecar_paths,
    )


